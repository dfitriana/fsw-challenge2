function sortCarByYearAscendingly(cars) {
  // Sangat dianjurkan untuk console.log semua hal hehe
  // console.log(cars);

  // Clone array untuk menghindari side-effect
  // Apa itu side effect?
  const result = [...cars];     // array result memiliki isi dari array cars
  
  // Tulis code-mu disini : menggunakan bubble sort

  const n = result.length                 // variabel untuk menyimpan nilai panjang array
  for (let i = 0; i < n; i++){            // 1. lakukan iterasi dari indeks 0 sampai panjang array
    for (let j = 0; j < n - 1; j++){      // 2. lakukan iterasi untuk elemen pembanding, larik selesai? step 1 : step 3
      if (result[j].year > result[j + 1].year) {      // 3. kemudian cek apakah elemen kiri > elemen kanan (kiri+1) ? lanjut step 4 : lenjut step 2 (membandingkan ke elemen selanjutnya)
        let temp = result[j]                          // 4. awalnya variabel temp berisi j
        result[j] = result[j + 1]                     // 5. karena bernilai true maka j ditukar dengan j+1
        result[j + 1] = temp                          // 6. karena ditukar true, nilai j+1, larik belum selesai? lanjut ke step 2 hingga larik selesai : tandai elemen ini sebagai elemen terurut dlm iterasi ini
      }
    }
  }

  // Rubah code ini dengan array hasil sorting secara ascending
  return result;
}

